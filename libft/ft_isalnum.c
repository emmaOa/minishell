#include "libft.h"

int	ft_isalnum(int c)
{
	if (48 <= c && c <= 57)
		return (1);
	else if ((34 <= c && c <= 90) || (91 <= c && c <= 126) || c == 32 \
			|| c == '<' || c == '>' || c == '-' || c == 39 || c == '"' \
			|| c == '$')
		return (1);
	else
		return (0);
}
